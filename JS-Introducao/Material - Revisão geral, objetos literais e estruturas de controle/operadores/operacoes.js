//adição

console.log(2 + 2);

//subtração

console.log(10 - 5);

//multiplicação

console.log(10 * 5);

//divisão

console.log(10 / 2);

//increment

let numero = 10;
console.log(++numero);

//decremento

console.log(--numero);
console.log(--numero);

//comparação de maior

console.log(10 > 5);

//comparação de menor

console.log(10 < 5);

//comparação de menor ou igual

console.log(3 <= 2);

//comparação de maior ou igual

console.log(20 >= 15);

//comparação de igualdade

console.log('ola' == 'ola');

//comparação de igualdade rígido

console.log(5 === '5');

//comparação de diferente

console.log('ola' != 'oi');

//comparação diferente rígido

console.log(10 !== 'ola');

//comparador lógico &&

console.log(10 > 5 && 8 > 10 && 11 > 5);

//comparador lógico ||

console.log(10 < 5 || 8 > 10);

//comparador de negação

console.log(!5 > 3);

//Operador de concatenar
let nome = 'João';
let sobrenome = 'Oliveira';

console.log(nome + ' ' + sobrenome);
